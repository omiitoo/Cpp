#include <iostream>
#include <string>

using namespace std;

int main()
{
    int myNum = 5;
    float myFloat = 5.99f;
    double myDouble = 9.98;
    char myLetter = 'D';
    char a = 65, b = 66, c = 67;
    bool myBooleanTrue = true;
    bool myBooleanFalse = false;
    string myString = "Hello";
    float f1 = 35e3;
    float d1 = 12E4;


    cout << "int: " << myNum << "\n";
    cout << "float: " << myFloat << "\n";
    cout << "double: " << myDouble << "\n";
    cout << "char: " << myLetter << "\n";
    cout << a << b << c << "\n";
    cout << "bool: " << myBooleanTrue << "\n";
    cout << "bool: " << myBooleanFalse << "\n";
    cout << "string: " << myString << "\n";
    cout << "f1: " << f1 << "\n";
    cout << "d1: " << d1 << "\n";


    return 0;
}
